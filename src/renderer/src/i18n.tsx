const DICTS: Record<Lang, Dict> = { tr: TR, en: EN }

interface I18nCtx {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string, params?: Record<string, string | number>) => string
}

const Ctx = createContext<I18nCtx | null>(null)

export function I18nProvider({ children }: { children: React.ReactNode }): React.JSX.Element {
  const { data, updateSettings } = useData()
  const lang: Lang = data.settings.lang ?? 'tr'

  const t = (key: string, params?: Record<string, string | number>): string => {
    let s = DICTS[lang][key] ?? DICTS.tr[key] ?? key
    if (params) {
      for (const [k, v] of Object.entries(params)) s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
    }
    return s
  }

  const setLang = (l: Lang): void => updateSettings({ lang: l })

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>
}

export function useI18n(): I18nCtx {
  const ctx = useContext(Ctx)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
